def load_data():
    """Load all CSV files and return as a dict of DataFrames."""
    files = {
        "latency" : "pe_latency_samples.csv",
        "comms"   : "pe_comms_overhead.csv",
        "poll"    : "pe_poll_distribution.csv",
        "summary" : "pe_summary.txt",
    }
    data = {}
    for key, fname in files.items():
        path = os.path.join(INPUT_DIR, fname)
        if not os.path.exists(path):
            print(f"[WARN] Missing file: {path}")
            continue
        if fname.endswith(".csv"):
            data[key] = pd.read_csv(path)
            print(f"  Loaded {fname}  ({len(data[key])} rows)")
        else:
            with open(path) as f:
                data[key] = f.read()
            print(f"  Loaded {fname}")
    return data


def save_fig(fig, name):
    """Save figure as both PDF (vector) and PNG (300 Dpi)."""
    os.makedirs(OUTPUT_DIR, exist_ok=True)
    pdf_path = os.path.join(OUTPUT_DIR, f"{name}.pdf")
    png_path = os.path.join(OUTPUT_DIR, f"{name}.png")
    fig.savefig(pdf_path, format="pdf")
    fig.savefig(png_path, format="png", dpi=300)
    print(f"  -> {pdf_path}  |  {png_path}")


def ns_to_cycles(ns):
    return ns / FPGA_PERIOD_NS


