# Figure 1 – Latency histogram with normal-fit overlay
# =============================================================================

def plot_latency_histogram(data):
    lat = data["latency"]["latency_ns"].values

    fig, ax = plt.subplots(figsize=(5.5, 3.5))

    # Histogram (normalized to density)
    n_bins = min(50, int(np.sqrt(len(lat))))
    ax.hist(lat, bins=n_bins, density=True,
            color=C_TOTAL, alpha=0.7, edgecolor="white", linewidth=0.4,
            label="Measured samples")

    # Normal distribution fit
    mu, sigma = stats.norm.fit(lat)
    x = np.linspace(lat.min(), lat.max(), 400)
    ax.plot(x, stats.norm.pdf(x, mu, sigma),
            color="black", linewidth=1.5, linestyle="--",
            label=f"Normal fit  μ={mu:.0f} ns,  σ={sigma:.0f} ns")

    # Percentile markers
    p50 = np.percentile(lat, 50)
    p95 = np.percentile(lat, 95)
    ax.axvline(p50, color=C_COMPUTE, linewidth=1.2, linestyle="-.",
               label=f"P50 = {p50:.0f} ns")
    ax.axvline(p95, color=C_COMMS,   linewidth=1.2, linestyle="-.",
               label=f"P95 = {p95:.0f} ns")

    ax.set_xlabel("Latency (ns)")
    ax.set_ylabel("Probability Density")
    ax.set_title("Fig. 1 – IP_PE Operation Latency Distribution\n"
                 "(1×1 PE, LW-HPS2FPGA bridge, Linux userspace)")
    ax.legend(loc="upper right", framealpha=0.9)

    # Secondary x-axis in clock cycles
    ax2 = ax.twiny()
    ax2.set_xlim(np.array(ax.get_xlim()) / FPGA_PERIOD_NS)
    ax2.set_xlabel("Clock Cycles (@50 MHz)")
    ax2.tick_params(labelsize=8)

    fig.tight_layout()
    save_fig(fig, "fig1_latency_histogram")
    plt.close(fig)


