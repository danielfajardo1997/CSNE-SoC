# =============================================================================

def plot_latency_cdf(data):
    lat = np.sort(data["latency"]["latency_ns"].values)
    cdf = np.arange(1, len(lat) + 1) / len(lat) * 100.0

    fig, ax = plt.subplots(figsize=(5.5, 3.5))

    ax.plot(lat, cdf, color=C_TOTAL, linewidth=1.8, label="Latency CDF")

    # Key percentile annotations
    for pct, color in [(50, C_COMPUTE), (90, C_SW), (95, C_COMMS), (99, "black")]:
        val = np.percentile(lat, pct)
        ax.axhline(pct, color=color, linewidth=0.8, linestyle="--", alpha=0.7)
        ax.axvline(val, color=color, linewidth=0.8, linestyle="--", alpha=0.7)
        ax.annotate(f"P{pct}\n{val:.0f} ns",
                    xy=(val, pct), xytext=(val + 200, pct - 8),
                    fontsize=7.5, color=color,
                    arrowprops=dict(arrowstyle="-", color=color, lw=0.6))

    ax.set_xlabel("Latency (ns)")
    ax.set_ylabel("Cumulative Probability (%)")
    ax.set_title("Fig. 2 – Latency CDF\n"
                 "(1×1 PE, 1 000 samples)")
    ax.set_ylim(0, 102)
    ax.yaxis.set_major_formatter(ticker.PercentFormatter())

    # Secondary x-axis in cycles
    ax2 = ax.twiny()
    ax2.set_xlim(np.array(ax.get_xlim()) / FPGA_PERIOD_NS)
    ax2.set_xlabel("Clock Cycles (@50 MHz)")
    ax2.tick_params(labelsize=8)

    ax.legend(loc="lower right")
    fig.tight_layout()
    save_fig(fig, "fig2_latency_cdf")
    plt.close(fig)


