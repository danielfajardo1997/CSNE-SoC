# Figure 6 – Throughput scaling projection (1×1 → N×N)
# =============================================================================

def plot_throughput_scaling(data):
    lat   = data["latency"]["latency_ns"].values
    comms = data["comms"]["comms_overhead_ns"].values

    measured_mops_1x1 = 0.1598   # from benchmark output

    # Theoretical model:
    # For an N×N array: N² MACs share ONE bridge transaction overhead.
    # Projected throughput = N² / (comms_mean + N² * compute_per_mac_ns) * 1e3
    mean_comms   = comms.mean()
    mean_compute = lat.mean() - mean_comms    # net compute for 1 MAC

    N_values = np.array([1, 2, 3, 4, 5, 6, 7, 8])
    macs     = N_values ** 2

    # Model: same comms overhead, compute scales with N²
    throughput_projected = macs / (mean_comms + macs * mean_compute) * 1e6  # MOPS

    # Ideal (no comms overhead)
    throughput_ideal = macs / (macs * mean_compute) * 1e6

    fig, ax = plt.subplots(figsize=(6, 4))

    ax.plot(macs, throughput_projected, "o-",
            color=C_PROJ, linewidth=2, markersize=6,
            label="Projected (with bridge overhead)")
    ax.plot(macs, throughput_ideal, "s--",
            color=C_COMPUTE, linewidth=1.5, markersize=5,
            label="Ideal (compute only, no comms)")
    ax.axhline(measured_mops_1x1, color=C_COMMS, linewidth=1.2,
               linestyle="-.", label=f"Measured 1×1 = {measured_mops_1x1:.3f} MOPS")

    # Annotate array sizes
    for n, mac, thr in zip(N_values, macs, throughput_projected):
        ax.annotate(f"{n}×{n}",
                    xy=(mac, thr), xytext=(mac + 0.3, thr * 1.06),
                    fontsize=8, color=C_PROJ)

    ax.set_xlabel("Number of MACs (N²  for N×N array)")
    ax.set_ylabel("Throughput (MOPS)")
    ax.set_title("Fig. 6 – Throughput Scaling Projection\n"
                 "(fixed bridge overhead amortised over N² MACs)")
    ax.legend(loc="upper left")
    ax.set_xlim(0, macs[-1] + 2)
    ax.set_ylim(0, throughput_ideal[-1] * 1.15)

    # Mark crossover with SW (1052 ns per op software)
    sw_mops = 1.0 / (1052.8e-9) / 1e6
    ax.axhline(sw_mops, color=C_SW, linewidth=1.2, linestyle=":",
               label=f"SW baseline = {sw_mops:.3f} MOPS")
    ax.legend(loc="upper left")

    fig.tight_layout()
    save_fig(fig, "fig6_throughput_scaling")
    plt.close(fig)


