def plot_dashboard(data):
    lat   = data["latency"]["latency_ns"].values
    comms = data["comms"]["comms_overhead_ns"].values

    fig = plt.figure(figsize=(12, 8))
    gs  = GridSpec(2, 3, figure=fig, hspace=0.45, wspace=0.38)

    # ── Top-left: histogram ──────────────────────────────────────────────
    ax1 = fig.add_subplot(gs[0, 0])
    n_bins = min(40, int(np.sqrt(len(lat))))
    ax1.hist(lat, bins=n_bins, density=True,
             color=C_TOTAL, alpha=0.75, edgecolor="white", linewidth=0.3)
    mu, sigma = stats.norm.fit(lat)
    x = np.linspace(lat.min(), lat.max(), 300)
    ax1.plot(x, stats.norm.pdf(x, mu, sigma), "k--", linewidth=1.2)
    ax1.set_title("Latency Distribution")
    ax1.set_xlabel("ns");  ax1.set_ylabel("Density")

    # ── Top-center: CDF ──────────────────────────────────────────────────
    ax2 = fig.add_subplot(gs[0, 1])
    lat_s = np.sort(lat)
    cdf   = np.arange(1, len(lat_s) + 1) / len(lat_s) * 100
    ax2.plot(lat_s, cdf, color=C_TOTAL, linewidth=1.5)
    for pct, col in [(50, C_COMPUTE), (95, C_COMMS)]:
        val = np.percentile(lat, pct)
        ax2.axvline(val, color=col, linewidth=0.9, linestyle="--",
                    label=f"P{pct}={val:.0f}ns")
    ax2.set_title("Latency CDF")
    ax2.set_xlabel("ns");  ax2.set_ylabel("%")
    ax2.yaxis.set_major_formatter(ticker.PercentFormatter())
    ax2.legend(fontsize=8)

    # ── Top-right: stacked bar ───────────────────────────────────────────
    ax3 = fig.add_subplot(gs[0, 2])
    mean_comms = comms.mean()
    mean_net   = lat.mean() - mean_comms
    bottom = 0
    for val, lbl, col, hat in [(mean_comms, "Comms", C_COMMS,   H_COMMS),
                                (mean_net,   "Compute", C_COMPUTE, H_COMPUTE)]:
        ax3.bar("1×1", val, bottom=bottom, color=col, edgecolor="black",
                linewidth=0.5, hatch=hat, label=lbl, width=0.4)
        ax3.text(0, bottom + val / 2, f"{val:.0f}ns",
                 ha="center", va="center", fontsize=8,
                 color="white", fontweight="bold")
        bottom += val
    ax3.set_title("Time Breakdown")
    ax3.set_ylabel("ns");  ax3.set_xticks([])
    ax3.legend(fontsize=8)

    # ── Bottom-left: box plot ────────────────────────────────────────────
    ax4 = fig.add_subplot(gs[1, 0])
    net = lat - mean_comms
    ax4.boxplot([lat, comms, net],
                labels=["Total", "Comms", "Compute"],
                patch_artist=True,
                boxprops=dict(facecolor="lightgrey"),
                medianprops=dict(color="black", linewidth=1.5),
                flierprops=dict(marker=".", markersize=2, alpha=0.3))
    ax4.set_title("Latency Box Plot")
    ax4.set_ylabel("ns")

    # ── Bottom-center: poll histogram ────────────────────────────────────
    ax5 = fig.add_subplot(gs[1, 1])
    poll_df = data["poll"]
    polls   = np.repeat(poll_df["poll_count"].values,
                        poll_df["frequency"].values)
    bins = np.arange(polls.min() - 0.5, polls.max() + 1.5, 1)
    ax5.hist(polls, bins=bins, color=C_POLL, edgecolor="white", linewidth=0.4)
    ax5.set_title("Polling Distribution")
    ax5.set_xlabel("Iterations");  ax5.set_ylabel("Count")

    # ── Bottom-right: scaling projection ────────────────────────────────
    ax6 = fig.add_subplot(gs[1, 2])
    N_values = np.arange(1, 9)
    macs     = N_values ** 2
    thr_proj = macs / (mean_comms + macs * mean_net) * 1e6
    ax6.plot(macs, thr_proj, "o-", color=C_PROJ, linewidth=1.8, markersize=5)
    ax6.axhline(1.0 / (1052.8e-9) / 1e6, color=C_SW, linewidth=1,
                linestyle=":", label="SW baseline")
    ax6.set_title("Throughput Scaling")
    ax6.set_xlabel("MACs (N²)");  ax6.set_ylabel("MOPS")
    ax6.legend(fontsize=8)

    fig.suptitle(
        "IP_PE Benchmark Dashboard  –  DE10-Nano  Cyclone V 5CSEBA6U23I7\n"
        "Operands: A=15, B=−7  |  50 MHz  |  LW-HPS2FPGA bridge  |  1 000 samples",
        fontsize=11, fontweight="bold"
    )

    save_fig(fig, "fig7_dashboard")
    plt.close(fig)


