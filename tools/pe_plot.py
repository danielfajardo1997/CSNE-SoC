def plot_poll_histogram(data):
    poll_df = data["poll"]
    polls   = np.repeat(poll_df["poll_count"].values,
                        poll_df["frequency"].values)

    fig, ax = plt.subplots(figsize=(5.5, 3.5))

    bins = np.arange(polls.min() - 0.5, polls.max() + 1.5, 1)
    ax.hist(polls, bins=bins, color=C_POLL, edgecolor="white",
            linewidth=0.5, density=False, label="Poll count frequency")

    mode_val = poll_df.loc[poll_df["frequency"].idxmax(), "poll_count"]
    ax.axvline(mode_val, color="black", linewidth=1.5, linestyle="--",
               label=f"Mode = {mode_val} iterations")

    ax.set_xlabel("ip_ready Polling Iterations per Operation")
    ax.set_ylabel("Frequency (out of 1 000 samples)")
    ax.set_title("Fig. 5 – ip_ready Polling Distribution\n"
                 "(validates FSM pipeline determinism)")
    ax.legend()

    # Annotate expected pipeline depth
    expected = int((5 + 2 + 3))  # MULT + SIGN + ADD cycles
    ax.axvline(expected, color=C_COMPUTE, linewidth=1.2, linestyle="-.",
               label=f"Expected pipeline depth ≈ {expected} cyc")
    ax.legend()

    fig.tight_layout()
    save_fig(fig, "fig5_poll_histogram")
    plt.close(fig)


