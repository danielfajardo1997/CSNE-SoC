# Fig 4 – Stacked bar + pie: time breakdown
# =============================================================================
def plot_stacked_bar(data):
    lat   = data["latency"]["latency_ns"].values
    comms = data["comms"]["comms_overhead_ns"].values

    mean_total  = lat.mean()
    mean_comms  = comms.mean()
    mean_net    = mean_total - mean_comms

    pct_comms   = 100.0 * mean_comms / mean_total
    pct_compute = 100.0 * mean_net   / mean_total

    fig, axes = plt.subplots(1, 2, figsize=(7, 3.5))

    # Left: absolute ns
    ax = axes[0]
    bottom = 0
    for val, lbl, col, hat in [(mean_comms, "Comms Overhead", C_COMMS, H_COMMS),
                                (mean_net,   "Net Compute",   C_COMPUTE, H_COMPUTE)]:
        ax.bar("3×3 Array", val, bottom=bottom,
               color=col, edgecolor="black", linewidth=0.6,
               hatch=hat, label=lbl, width=0.4)
        ax.text(0, bottom + val / 2,
                f"{val:.0f} ns\n({ns_to_cycles(val):.0f} cyc)",
                ha="center", va="center", fontsize=8.5,
                color="white", fontweight="bold")
        bottom += val

    ax.set_ylabel("Time per Matrix Multiply (ns)")
    ax.set_title("Absolute Time Breakdown")
    ax.set_ylim(0, mean_total * 1.15)
    ax.legend(loc="upper right", framealpha=0.9)
    ax.set_xticks([])

    # Right: pie
    ax2 = axes[1]
    wedges, texts, autotexts = ax2.pie(
        [pct_comms, pct_compute],
        labels=["Comms\nOverhead", "Net\nCompute"],
        colors=[C_COMMS, C_COMPUTE],
        autopct="%1.1f%%", startangle=90,
        wedgeprops=dict(edgecolor="black", linewidth=0.8),
        textprops=dict(fontsize=9),
    )
    for at in autotexts:
        at.set_fontsize(9); at.set_fontweight("bold")
    ax2.set_title("Percentage Breakdown")

    fig.suptitle("Fig. 4 – Time Breakdown per Matrix Multiply  (3×3 Array)",
                 fontsize=11, fontweight="bold", y=1.02)
    fig.tight_layout()
    save_fig(fig, "fig4_time_breakdown")
    plt.close(fig)


