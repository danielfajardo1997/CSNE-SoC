def load_data():
    files = {
        "latency" : "sa3x3_latency_samples.csv",
        "comms"   : "sa3x3_comms_overhead.csv",
        "poll"    : "sa3x3_poll_distribution.csv",
    }
    data = {}
    for key, fname in files.items():
        path = os.path.join(INPUT_DIR, fname)
        if not os.path.exists(path):
            print(f"[WARN] Missing: {path}")
            continue
        data[key] = pd.read_csv(path)
        print(f"  Loaded {fname}  ({len(data[key])} rows)")
    return data


def save_fig(fig, name):
    os.makedirs(OUTPUT_DIR, exist_ok=True)
    pdf = os.path.join(OUTPUT_DIR, f"{name}.pdf")
    png = os.path.join(OUTPUT_DIR, f"{name}.png")
    fig.savefig(pdf, format="pdf")
    fig.savefig(png, format="png", dpi=300)
    print(f"  -> {pdf}  |  {png}")


def ns_to_cycles(ns):
    return ns / FPGA_PERIOD_NS


def add_cycle_axis(ax):
    """Add secondary x-axis in clock cycles."""
    ax2 = ax.twiny()
    ax2.set_xlim(np.array(ax.get_xlim()) / FPGA_PERIOD_NS)
    ax2.set_xlabel("Clock Cycles (@50 MHz)")
    ax2.tick_params(labelsize=8)
    return ax2


