# Fig 1 – Latency histogram with normal-fit
# =============================================================================
def plot_latency_histogram(data):
    lat = data["latency"]["latency_ns"].values

    fig, ax = plt.subplots(figsize=(5.5, 3.5))

    n_bins = min(50, int(np.sqrt(len(lat))))
    ax.hist(lat, bins=n_bins, density=True,
            color=C_TOTAL, alpha=0.7, edgecolor="white", linewidth=0.4,
            label="Measured samples")

    mu, sigma = stats.norm.fit(lat)
    x = np.linspace(lat.min(), lat.max(), 400)
    ax.plot(x, stats.norm.pdf(x, mu, sigma), "k--", linewidth=1.5,
            label=f"Normal fit  μ={mu:.0f} ns,  σ={sigma:.0f} ns")

    for pct, color, ls in [(50, C_COMPUTE, "-."), (95, C_COMMS, "-.")]:
        val = np.percentile(lat, pct)
        ax.axvline(val, color=color, linewidth=1.2, linestyle=ls,
                   label=f"P{pct} = {val:.0f} ns")

    ax.set_xlabel("Latency per 3×3 Matrix Multiply (ns)")
    ax.set_ylabel("Probability Density")
    ax.set_title("Fig. 1 – 3×3 Array Latency Distribution\n"
                 "(1 000 matrix multiplies, flush each, LW-HPS2FPGA bridge)")
    ax.legend(loc="upper right", framealpha=0.9)
    add_cycle_axis(ax)

    fig.tight_layout()
    save_fig(fig, "fig1_latency_histogram")
    plt.close(fig)


