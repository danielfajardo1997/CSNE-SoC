# Fig 3 – Box plot: latency breakdown
# =============================================================================
def plot_boxplot_breakdown(data):
    lat   = data["latency"]["latency_ns"].values
    comms = data["comms"]["comms_overhead_ns"].values
    net   = lat - comms.mean()

    df = pd.DataFrame({
        "Latency (ns)" : np.concatenate([lat, comms, net]),
        "Component"    : (["Total Round-Trip"] * len(lat) +
                          ["Comms Overhead"]   * len(comms) +
                          ["Net Compute"]      * len(net)),
    })

    fig, ax = plt.subplots(figsize=(6, 4))

    palette = {"Total Round-Trip": C_TOTAL,
               "Comms Overhead"  : C_COMMS,
               "Net Compute"     : C_COMPUTE}

    sns.boxplot(data=df, x="Component", y="Latency (ns)",
                palette=palette, width=0.5, linewidth=1.2,
                flierprops=dict(marker=".", markersize=2, alpha=0.4),
                ax=ax)

    for i, comp in enumerate(["Total Round-Trip", "Comms Overhead", "Net Compute"]):
        med = df[df["Component"] == comp]["Latency (ns)"].median()
        ax.text(i, med + 200, f"{med:.0f} ns\n({ns_to_cycles(med):.0f} cyc)",
                ha="center", va="bottom", fontsize=8, fontweight="bold")

    ax.set_title("Fig. 3 – Latency Breakdown: Total vs. Comms vs. Net Compute\n"
                 "(3×3 Array, 1 000 samples each)")
    ax.set_ylabel("Latency (ns)")
    ax.set_xlabel("")

    for patch, hatch in zip(ax.patches, [H_TOTAL, H_COMMS, H_COMPUTE]):
        patch.set_hatch(hatch)

    fig.tight_layout()
    save_fig(fig, "fig3_boxplot_breakdown")
    plt.close(fig)


